// package: 클래스 파일의 위치를 나타내는 코드
// 여기는 default package 니까 일단은 안씀

// public class "file name"로 시작
// 클래스 이름은 파스칼 표기법으로: 모든 단어의 첫글자를 대문자로
public class MyMain {
	// public static void: 자바 코드가 실행되는 시작부분. 반드시 고정.
	public static void main(String[] args) {
		// 콘솔창에 괄호 안의 내용을 출력하는 기능
		System.out.println("Hello world");
		//syso를 입력 후 ctrl+space 자동완성 가능
		System.out.println("Java");
	}

}
